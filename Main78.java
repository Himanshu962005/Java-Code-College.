// Write the following expression in a Java program : (v^2 - u^2) / (2as).
class Main78 { // ExpressionFormula.
    public static void main(String[] args) {
        int v = 10;
        int u = 5;
        int a = 2;
        int s = 4;
        int result = (v * v - u * u) / (2 * a * s);
        System.out.println("Result = " + result);
    }
}